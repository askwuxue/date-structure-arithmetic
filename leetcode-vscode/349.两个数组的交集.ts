// @lc code=start
function intersection(nums1: number[], nums2: number[]): number[] {
  const set = new Set<number>(nums1);
  const res = new Set<number>();
  for (const num of nums2) {
    if (set.has(num) && !res.has(num)) {
      res.add(num);
    }
  }
  return [...res.values()];
}
